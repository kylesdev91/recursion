var factorialNumber = n
var newNumber = 1

repeat {
    print("Calculating factorial(\(factorialNumber))...")
    factorialNumber -= 1
} while (factorialNumber >= 1)
print("Terminal case, returning 1.")

factorialNumber = 2

if n > 1 {
    while factorialNumber <= n {
        newNumber *= factorialNumber
        print("Non-terminal case, returning \(newNumber).")
        factorialNumber += 1
    }
}
